def code(emoji):
    return "_".join(f"{ord(c):x}" for c in emoji if ord(c) != 0xFE0F)

def download(c):
    path = os.path.join(EMOJI_DIR, f"{c}.svg")
    if os.path.exists(path) and os.path.getsize(path) > 0:
        return True
    try:
        req = urllib.request.Request(BASE.format(c), headers={"User-Agent": "curl/8"})
        data = urllib.request.urlopen(req, timeout=20).read()
        if not data:
            return False
        with open(path, "wb") as f:
            f.write(data)
        return True
    except Exception as e:
        sys.stderr.write(f"  fail {c}: {e}\n")
        return False

os.makedirs(EMOJI_DIR, exist_ok=True)

def process(pairs, label):
    kept, dropped = [], []
    seen = set()
    for w, e in pairs:
        if w in seen:
            dropped.append((w, e, "dup-word"))
            continue
        seen.add(w)
        c = code(e)
        if download(c):
            kept.append((w, e))
        else:
            dropped.append((w, e, "no-svg"))
    print(f"{label}: kept {len(kept)}, dropped {len(dropped)}")
    for w, e, why in dropped:
        print(f"   dropped {w} {e} ({why})")
    return kept

hira = process(HIRA, "hira")
kata = process(KATA, "kata")
hangul = process(HANGUL, "hangul")

def render(pairs):
    width = max(len(w) for w, _ in pairs) + 2
    lines = []
    for w, e in pairs:
        wq = f'"{w}",'
        lines.append(f'    {{ w: {wq:<{width+2}} e: "{e}" }},')
    return "\n".join(lines)

block = (
    "const WORDS = {\n"
    "  hira: [\n" + render(hira) + "\n  ],\n"
    "  kata: [\n" + render(kata) + "\n  ],\n"
    "  hangul: [\n" + render(hangul) + "\n  ],\n"
    "};"
)

html = open(HTML, encoding="utf-8").read()
new, n = re.subn(r"const WORDS = \{.*?\n\};", block, html, count=1, flags=re.S)
assert n == 1, "WORDS block not found in index.html!"
open(HTML, "w", encoding="utf-8").write(new)
print(f"\nTOTAL kept: hira {len(hira)} + kata {len(kata)} + hangul {len(hangul)} = {len(hira)+len(kata)+len(hangul)}")
